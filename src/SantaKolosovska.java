public class SantaKolosovska {
    public static void main(String[] args) {
        int[] myArray = {1, 6, 3, 50, 3, 12};

        for (int i = 0; i < myArray.length; i++) {
            System.out.println(myArray[i]);
        }

        for (int i = myArray.length - 1; i >= 0; i--) {
            System.out.println(myArray[i]);
        }


    }
}
